// *** switch *** 

const month = 3; 

switch(month) {
    case 1: // or case "jan": 
        console.log("January");
        break;
    case 2: 
        console.log("february");
        break;
    case 3: 
        console.log("March");
        break;
    default: 
        console.log("default case matched");
        break;
}